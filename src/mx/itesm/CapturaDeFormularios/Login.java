    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        user = jTextField1.getText();
        password = jPasswordField1.getText();
        String tmpPassword = "";
        
        try {
            Connection con = Conexion.getconexion();
        
            String query = "SELECT idUsuario, password FROM Usuarios WHERE nombre = \"" + user + "\";";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            
            while (rs.next()) {
                tmpPassword = rs.getString("password");
                idUsuario = Integer.parseInt(rs.getString("idUsuario"));
            }
            
            con.close();
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
        
        if (password == null ? tmpPassword == null : password.equals(tmpPassword)) {
            login = true;
        }
            
        if (login){
            OpenFormula.Instance().setVisible(true);
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(frame,
                                          "No de ha podido iniciar sesi칩n. Usuario y contrase침a incorrectos o no existen.",
                                          "Error.",
                                          JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

