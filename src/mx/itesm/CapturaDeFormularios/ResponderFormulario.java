    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            DefaultListModel model = new DefaultListModel();
            
            String query = "SELECT * FROM formularios";
            Connection con = Conexion.getconexion();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            String idFormulario, nombre, descripcion;
            
            while (rs.next()) {
                idFormulario = rs.getString("idFormulario");
                nombre = rs.getString("nombre");
                descripcion = rs.getString("descripcion");
                model.addElement(idFormulario + "       Nombre: " + nombre + "       Descripcion: " + descripcion);
            }
            
            jList1.setModel(model);
            st.close();
            con.close();
            
        } catch (SQLException ex) {
            Logger.getLogger(ResponderFormulario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

